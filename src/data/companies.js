// Build search index: normalised ticker + name for fast fuzzy match
const _idx = COMPANIES.map(([ticker, name, exchange, sector]) => ({
  ticker,
  name,
  exchange,
  sector,
  _search: `${ticker} ${name}`.toLowerCase(),
}));

/**
 * Search companies by ticker or name fragment.
 * Returns up to `limit` results sorted by relevance.
 */
export function searchCompanies(query, limit = 8) {
  if (!query || query.trim().length < 1) return [];
  const q = query.trim().toLowerCase();

  const exact   = [];
  const starts  = [];
  const contains = [];

  for (const c of _idx) {
    const t = c.ticker.toLowerCase();
    const n = c.name.toLowerCase();
    if (t === q || n === q)                  { exact.push(c);    continue; }
    if (t.startsWith(q) || n.startsWith(q)) { starts.push(c);   continue; }
    if (c._search.includes(q))              { contains.push(c); continue; }
  }

  return [...exact, ...starts, ...contains].slice(0, limit);
}
